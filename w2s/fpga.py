"""
fpga.py -- FPGA targeting: estimation, synthesis attributes, and build scripts.

While the Verilog generated by w2s is already synthesizable on FPGAs,
this module provides FPGA-specific optimizations and tooling:

- FPGA resource estimation (LUTs, BRAMs, DSP slices)
- Synthesis attribute hints for block RAM and DSP inference
- Build script generation for open-source FPGA toolchains
- Pin constraint file templates for popular boards
"""

import math
from dataclasses import dataclass, field
from pathlib import Path
from typing import List, Dict, Optional

import numpy as np

from w2s.core import ComputeGraph, Operation, OpType


# ---------------------------------------------------------------------------
#  FPGA device definitions
# ---------------------------------------------------------------------------

@dataclass
class FPGADevice:
    """FPGA device resource specifications."""
    name: str
    family: str
    lut4s: int           # total LUT4 equivalents
    ffs: int             # total flip-flops
    bram_18k: int        # 18Kbit block RAMs
    bram_bits: int       # total BRAM bits
    dsp_slices: int      # DSP multiplier blocks
    dsp_width: int       # native DSP multiplier width (e.g., 18 for DSP48)
    max_freq_mhz: int    # typical max clock frequency
    package: str = ""    # device package (e.g., "sg48", "CABGA381")


