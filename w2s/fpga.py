# ---------------------------------------------------------------------------
#  FPGA resource estimation
# ---------------------------------------------------------------------------

@dataclass
class FPGAEstimate:
    """FPGA-specific resource estimates."""
    device: FPGADevice
    mode: str
    lut4s_used: int
    ffs_used: int
    bram_bits_used: int
    bram_blocks_used: int
    dsp_slices_used: int
    fits: bool
    utilization_lut: float
    utilization_bram: float
    utilization_dsp: float
    warnings: List[str] = field(default_factory=list)
    _breakdown: Dict[str, int] = field(default_factory=dict, repr=False)

    def __str__(self) -> str:
        lines = []
        lines.append(f"=== FPGA Estimate: {self.device.name} ({self.mode} mode) ===")
        lines.append(f"LUT4s:       {self.lut4s_used:,} / {self.device.lut4s:,} "
                     f"({self.utilization_lut:.0%})")
        lines.append(f"Flip-flops:  {self.ffs_used:,} / {self.device.ffs:,}")
        lines.append(f"BRAM bits:   {self.bram_bits_used:,} / {self.device.bram_bits:,} "
                     f"({self.utilization_bram:.0%})")
        if self.device.dsp_slices > 0:
            lines.append(f"DSP slices:  {self.dsp_slices_used:,} / {self.device.dsp_slices:,} "
                         f"({self.utilization_dsp:.0%})")
        else:
            lines.append(f"DSP slices:  N/A (device has no hard multipliers)")

        if self.fits:
            lines.append(f"Status:      FITS")
        else:
            over = []
            if self.lut4s_used > self.device.lut4s:
                over.append("LUTs")
            if self.bram_bits_used > self.device.bram_bits:
                over.append("BRAM")
            if self.dsp_slices_used > self.device.dsp_slices and self.device.dsp_slices > 0:
                over.append("DSP")
            lines.append(f"Status:      DOES NOT FIT (exceeds: {', '.join(over)})")

        if self.warnings:
            lines.append("")
            for w in self.warnings:
                lines.append(f"  - {w}")

        return "\n".join(lines)


def estimate_fpga(
    graph: ComputeGraph,
    device: FPGADevice = None,
    mode: str = "combinational",
) -> FPGAEstimate:
    """
    Estimate FPGA resource usage for a compiled design.

    In sequential mode, weights go to BRAM and a single DSP handles MAC.
    In combinational mode, weights become LUT logic with optional DSP for
    large multipliers.
    """
    if mode not in ("combinational", "sequential"):
        raise ValueError(f"Unknown mode '{mode}'; expected 'combinational' or 'sequential'")

    if device is None:
        device = ICE40_UP5K

    bits = graph.quant_config.bits
    ops = graph.topological_order()
    warnings = []

    # Count parameters
    total_params = 0
    total_multiplies = 0
    for op in ops:
        weights = op.q_weights if op.q_weights else op.weights
        for w in weights.values():
            total_params += int(np.prod(w.shape))
        if op.op_type in (OpType.DENSE, OpType.CONV1D, OpType.CONV2D):
            w = weights.get('weight')
            if w is not None:
                total_multiplies += int(np.count_nonzero(w))

    rom_bits = total_params * bits

    if mode == "sequential":
        return _estimate_fpga_sequential(
            graph, device, ops, total_params, total_multiplies,
            rom_bits, bits, warnings)
    else:
        return _estimate_fpga_combinational(
            graph, device, ops, total_params, total_multiplies,
            rom_bits, bits, warnings)


def _estimate_fpga_sequential(graph, device, ops, total_params,
                               total_multiplies, rom_bits, bits, warnings):
    """Sequential mode: weights in BRAM, single MAC in DSP or LUTs."""
    # BRAM usage: all weights stored in block RAM
    bram_bits_used = rom_bits

    # MAC engine: 1 DSP slice if device supports it, otherwise LUTs
    if device.dsp_slices > 0 and bits <= device.dsp_width:
        dsp_used = 1
        mac_luts = 50  # control logic only
    else:
        dsp_used = 0
        mac_luts = 200 + 32  # multiplier + accumulator in LUTs

    # Control logic + state machine + buffers
    control_luts = 80
    # Buffer for largest intermediate activation
    max_buf = 0
    for op in ops:
        weights = op.q_weights if op.q_weights else op.weights
        w = weights.get('weight')
        if w is not None:
            max_buf = max(max_buf, w.shape[0])
    buffer_ffs = max_buf * bits + 80  # activation buffer + state regs

    lut4s_used = mac_luts + control_luts
    # If BRAM can't hold all weights, spill to LUTs
    if bram_bits_used > device.bram_bits:
        spill_bits = bram_bits_used - device.bram_bits
        lut4s_used += spill_bits // 4  # ~4 bits per LUT4 for ROM
        warnings.append(f"Weights exceed BRAM ({rom_bits:,} bits > "
                       f"{device.bram_bits:,} bits). "
                       f"{spill_bits:,} bits spill to LUT fabric.")
    bram_blocks = math.ceil(bram_bits_used / 18432) if device.bram_18k > 0 else 0

    fits = (lut4s_used <= device.lut4s and
            dsp_used <= device.dsp_slices and
            bram_bits_used <= device.bram_bits)

    return FPGAEstimate(
        device=device,
        mode="sequential",
        lut4s_used=lut4s_used,
        ffs_used=buffer_ffs,
        bram_bits_used=min(bram_bits_used, device.bram_bits),
        bram_blocks_used=bram_blocks,
        dsp_slices_used=dsp_used,
        fits=fits,
        utilization_lut=lut4s_used / device.lut4s if device.lut4s else 0,
        utilization_bram=bram_bits_used / device.bram_bits if device.bram_bits else 0,
        utilization_dsp=dsp_used / device.dsp_slices if device.dsp_slices else 0,
        warnings=warnings,
    )


def _estimate_fpga_combinational(graph, device, ops, total_params,
                                  total_multiplies, rom_bits, bits, warnings):
    """Combinational mode: weights as LUT logic, optional DSP for multipliers."""
    bit_scale = bits / 8.0

    # Each constant multiply: ~6 LUT4s per bit of the constant
    # (shift-add decomposition maps to LUT chains)
    luts_per_mul = int(24 * bit_scale)
    luts_per_add = int(8 * bit_scale)

    mul_luts = total_multiplies * luts_per_mul
    add_luts = 0

    # Count requant and saturation
    n_neurons = 0
    for op in ops:
        weights = op.q_weights if op.q_weights else op.weights
        w = weights.get('weight')
        if w is not None:
            n_neurons += w.shape[0]
            add_luts += max(int(np.count_nonzero(w)) - w.shape[0], 0) * luts_per_add

    requant_luts = n_neurons * 64
    sat_luts = n_neurons * 8

    # DSP offloading: if device has DSPs, use them for the largest multipliers
    dsp_used = 0
    dsp_savings = 0
    if device.dsp_slices > 0 and bits <= device.dsp_width:
        # Each DSP can handle one full multiply.  Use DSPs for requantization
        # multipliers (one per neuron output) since those are the widest.
        dsp_used = min(n_neurons, device.dsp_slices)
        dsp_savings = dsp_used * 64  # each DSP replaces ~64 LUTs of requant logic
        requant_luts = max(0, requant_luts - dsp_savings)

    lut4s_used = mul_luts + add_luts + requant_luts + sat_luts

    fits = (lut4s_used <= device.lut4s and
            dsp_used <= device.dsp_slices)

    if not fits:
        warnings.append(
            f"Design needs ~{lut4s_used:,} LUT4s but device has {device.lut4s:,}. "
            f"Consider sequential mode or a larger FPGA.")

    return FPGAEstimate(
        device=device,
        mode="combinational",
        lut4s_used=lut4s_used,
        ffs_used=0,  # combinational: no FFs
        bram_bits_used=0,  # combinational: weights in LUTs
        bram_blocks_used=0,
        dsp_slices_used=dsp_used,
        fits=fits,
        utilization_lut=lut4s_used / device.lut4s if device.lut4s else 0,
        utilization_bram=0,
        utilization_dsp=dsp_used / device.dsp_slices if device.dsp_slices else 0,
        warnings=warnings,
        _breakdown={
            "mul_luts": mul_luts,
            "add_luts": add_luts,
            "requant_luts": requant_luts,
            "sat_luts": sat_luts,
            "dsp_savings": dsp_savings,
        },
    )


# ---------------------------------------------------------------------------
#  Build script generation
# ---------------------------------------------------------------------------

def generate_build_script(
    graph: ComputeGraph,
    device: FPGADevice = None,
    output_dir: str = ".",
    mode: str = "combinational",
    pcf_file: Optional[str] = None,
) -> str:
    """
    Generate a Makefile for open-source FPGA synthesis.

    Supports:
      - iCE40 family: Yosys -> nextpnr-ice40 -> icepack -> iceprog
      - ECP5 family:  Yosys -> nextpnr-ecp5 -> ecppack -> openFPGALoader
    """
    if device is None:
        device = ICE40_UP5K

    out = Path(output_dir)
    out.mkdir(parents=True, exist_ok=True)

    if mode == "sequential":
        top_module = f"{graph.name}_seq"
        verilog_files = [f"{graph.name}_seq.v"]
    else:
        top_module = graph.name
        verilog_files = [f"{graph.name}.v"]

    vfiles = " ".join(verilog_files)

    lines = []
    lines.append(f"# Makefile for {graph.name} — generated by weights2silicon (w2s)")
    lines.append(f"# Target: {device.name} ({device.family})")
    lines.append(f"# Mode: {mode}")
    lines.append(f"")
    lines.append(f"TOP      = {top_module}")
    lines.append(f"VERILOG  = {vfiles}")

    if pcf_file:
        lines.append(f"PCF      = {pcf_file}")
    elif device.family == "ecp5":
        lines.append(f"PCF      = pins.lpf")
    else:
        lines.append(f"PCF      = pins.pcf")

    lines.append(f"")

    if device.family == "ice40":
        pkg = device.package or "sg48"
        if "hx8k" in device.name.lower():
            dev_flag = "hx8k"
        elif "up5k" in device.name.lower():
            dev_flag = "up5k"
        else:
            dev_flag = device.name.lower().replace("ice40", "")

        lines.append(f"# iCE40 toolchain: Yosys + nextpnr-ice40 + icepack")
        lines.append(f"")
        lines.append(f"all: $(TOP).bin")
        lines.append(f"")
        lines.append(f"# Synthesis")
        lines.append(f"$(TOP).json: $(VERILOG)")
        lines.append(f"\tyosys -p 'synth_ice40 -top $(TOP) -json $@' $(VERILOG)")
        lines.append(f"")
        lines.append(f"# Place and route")
        lines.append(f"$(TOP).asc: $(TOP).json $(PCF)")
        lines.append(f"\tnextpnr-ice40 --{dev_flag} --package {pkg} "
                     f"--json $(TOP).json --pcf $(PCF) --asc $@")
        lines.append(f"")
        lines.append(f"# Bitstream")
        lines.append(f"$(TOP).bin: $(TOP).asc")
        lines.append(f"\ticepack $(TOP).asc $@")
        lines.append(f"")
        lines.append(f"# Program")
        lines.append(f"prog: $(TOP).bin")
        lines.append(f"\ticeprog $(TOP).bin")
        lines.append(f"")
        lines.append(f"# Timing report")
        lines.append(f"timing: $(TOP).asc")
        lines.append(f"\ticetime -d {dev_flag} $(TOP).asc")
        lines.append(f"")

    elif device.family == "ecp5":
        if "25k" in device.name.lower():
            dev_flag = "25k"
        elif "85k" in device.name.lower():
            dev_flag = "85k"
        else:
            dev_flag = "45k"

        lines.append(f"# ECP5 toolchain: Yosys + nextpnr-ecp5 + ecppack")
        lines.append(f"")
        lines.append(f"all: $(TOP).bit")
        lines.append(f"")
        lines.append(f"# Synthesis")
        lines.append(f"$(TOP).json: $(VERILOG)")
        lines.append(f"\tyosys -p 'synth_ecp5 -top $(TOP) -json $@' $(VERILOG)")
        lines.append(f"")
        lines.append(f"# Place and route")
        lines.append(f"$(TOP).config: $(TOP).json $(PCF)")
        lines.append(f"\tnextpnr-ecp5 --{dev_flag} --package {device.package or 'CABGA381'} "
                     f"--json $(TOP).json --lpf $(PCF) --textcfg $@")
        lines.append(f"")
        lines.append(f"# Bitstream")
        lines.append(f"$(TOP).bit: $(TOP).config")
        lines.append(f"\tecppack --compress $< $@")
        lines.append(f"")
        lines.append(f"# Program")
        lines.append(f"prog: $(TOP).bit")
        lines.append(f"\topenFPGALoader $(TOP).bit")
        lines.append(f"")

    # Common targets
    lines.append(f"# Resource usage report (synthesis only)")
    lines.append(f"report: $(TOP).json")
    lines.append(f"\t@echo 'See yosys synthesis log for resource usage'")
    lines.append(f"")
    lines.append(f"# Simulation (Icarus Verilog)")
    lines.append(f"sim: $(VERILOG) $(TOP)_tb.v")
    lines.append(f"\tiverilog -o $(TOP)_sim $(VERILOG) $(TOP)_tb.v")
    lines.append(f"\tvvp $(TOP)_sim")
    lines.append(f"")
    lines.append(f"clean:")
    lines.append(f"\trm -f $(TOP).json $(TOP).asc $(TOP).config "
                 f"$(TOP).bin $(TOP).bit $(TOP)_sim *.vcd")
    lines.append(f"")
    lines.append(f".PHONY: all prog timing report sim clean")

    mk_path = out / "Makefile"
    mk_path.write_text("\n".join(lines), encoding="utf-8")
    return str(mk_path)


