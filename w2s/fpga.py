def generate_constraints(
    graph: ComputeGraph,
    device: FPGADevice = None,
    output_dir: str = ".",
    mode: str = "combinational",
) -> str:
    """
    Generate a pin constraint template file.

    Creates a .pcf (iCE40) or .lpf (ECP5) file with placeholder pin
    assignments for the design's I/O signals.
    """
    if device is None:
        device = ICE40_UP5K

    out = Path(output_dir)
    out.mkdir(parents=True, exist_ok=True)
    bits = graph.quant_config.bits

    lines = []
    lines.append(f"# Pin constraints for {graph.name}")
    lines.append(f"# Target: {device.name}")
    lines.append(f"# Generated by weights2silicon (w2s)")
    lines.append(f"#")
    lines.append(f"# IMPORTANT: Replace pin numbers with your actual board pinout!")
    lines.append(f"#")
    lines.append(f"")

    if mode == "sequential":
        if device.family == "ice40":
            lines.append(f"# Clock and reset")
            lines.append(f"set_io clk        35")
            lines.append(f"set_io rst_n      2")
            lines.append(f"")
            lines.append(f"# Serial data interface")
            for i in range(bits):
                lines.append(f"set_io data_in[{i}]  {10 + i}  # REPLACE")
            lines.append(f"set_io data_valid  {10 + bits}  # REPLACE")
            lines.append(f"")
            for i in range(bits):
                lines.append(f"set_io data_out[{i}] {20 + i}  # REPLACE")
            lines.append(f"set_io out_valid   {20 + bits}  # REPLACE")
            lines.append(f"set_io done        {20 + bits + 1}  # REPLACE")
            lines.append(f"set_io ready       {20 + bits + 2}  # REPLACE")

        elif device.family == "ecp5":
            lines.append(f"LOCATE COMP \"clk\"        SITE \"P6\";")
            lines.append(f"LOCATE COMP \"rst_n\"      SITE \"T1\";")
            lines.append(f"IOBUF PORT \"clk\" IO_TYPE=LVCMOS33;")
            lines.append(f"IOBUF PORT \"rst_n\" IO_TYPE=LVCMOS33;")
            lines.append(f"")
            lines.append(f"# Serial data interface — REPLACE pin sites")
            for i in range(bits):
                lines.append(f"LOCATE COMP \"data_in[{i}]\" SITE \"REPLACE\";")
            lines.append(f"LOCATE COMP \"data_valid\" SITE \"REPLACE\";")
            for i in range(bits):
                lines.append(f"LOCATE COMP \"data_out[{i}]\" SITE \"REPLACE\";")
            lines.append(f"LOCATE COMP \"out_valid\" SITE \"REPLACE\";")
            lines.append(f"LOCATE COMP \"done\" SITE \"REPLACE\";")
            lines.append(f"LOCATE COMP \"ready\" SITE \"REPLACE\";")

    else:  # combinational mode: parallel I/O ports
        # Build list of input wire names from graph
        input_wires = []
        for inp_name in graph.input_names:
            shape = graph.input_shapes.get(inp_name, (1,))
            numel = 1
            for s in shape:
                numel *= s
            for i in range(numel):
                input_wires.append(f"{inp_name}_{i}")

        # Build list of output wire names from graph
        output_wires = []
        for out_name in graph.output_names:
            # Mirror the naming convention from graph.py
            # For single output: out_0, out_1, ...
            # For multiple outputs: out_{name}_0, out_{name}_1, ...
            # Use a reasonable default count based on graph info
            multi = len(graph.output_names) > 1
            prefix = f"out_{out_name}" if multi else "out"
            # We don't know the exact output width here, so emit one
            # placeholder per output name; users extend as needed.
            output_wires.append(f"{prefix}_0")

        if device.family == "ice40":
            lines.append(f"# Combinational inputs")
            for idx, w in enumerate(input_wires):
                lines.append(f"set_io {w}  {10 + idx}  # REPLACE")
            lines.append(f"")
            lines.append(f"# Combinational outputs")
            for idx, w in enumerate(output_wires):
                lines.append(f"set_io {w}  {30 + idx}  # REPLACE")

        elif device.family == "ecp5":
            lines.append(f"# Combinational inputs — REPLACE pin sites")
            for w in input_wires:
                lines.append(f"LOCATE COMP \"{w}\" SITE \"REPLACE\";")
            lines.append(f"")
            lines.append(f"# Combinational outputs — REPLACE pin sites")
            for w in output_wires:
                lines.append(f"LOCATE COMP \"{w}\" SITE \"REPLACE\";")

    ext = "pcf" if device.family == "ice40" else "lpf"
    pcf_path = out / f"pins.{ext}"
    pcf_path.write_text("\n".join(lines), encoding="utf-8")
    return str(pcf_path)
